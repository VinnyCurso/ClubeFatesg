    private void carregarCombo() {
        try {

            ETipoAssociado item0 = new ETipoAssociado(0, "Selecione...");
            cmbTipoAssociado.addItem(item0);

            for (ETipoAssociado item : new NTipoAssociado().listar()) {
                cmbTipoAssociado.addItem(item);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void limparTela() {
        try {
            txtIdentificador.setText("");
            txtNome.setText("");
            txtEndereco.setText("");

            cmbTipoAssociado.setSelectedIndex(0);

            btnExcluir.setEnabled(false);

        } catch (Exception e) {
        }
    }

    private void preencherTela(EAssociado associado) throws Exception {

        if (associado.getCodigo() == 0) {
            throw new Exception("Associado não encontrado!");
        }

        txtIdentificador.setText(associado.getCodigo() + "");
        txtNome.setText(associado.getNome());
        txtEndereco.setText(associado.getEndereco());

        for (int i = 0; i < cmbTipoAssociado.getModel().getSize(); i++) {

            ETipoAssociado item = (ETipoAssociado) cmbTipoAssociado.getModel()
                    .getElementAt(i);

            if (associado.getTipoAssociado().getCodigo() == item.getCodigo()) {
                cmbTipoAssociado.setSelectedIndex(i);
            }
        }

        btnExcluir.setEnabled(true);

    }
