    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        try {

            String codigo = JOptionPane.showInputDialog("Digite o código do associado:");
            int intCodigo = Integer.parseInt(codigo);
            EAssociado associado = new NAssociado().consultar(intCodigo);

            preencherTela(associado);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Digite um código válido para consulta.");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {

            EAssociado associado = new EAssociado();

            if (!txtIdentificador.getText().isEmpty()) {
                associado.setCodigo(Integer.parseInt(txtIdentificador.getText()));
            }

            associado.setNome(txtNome.getText());
            associado.setEndereco(txtEndereco.getText());

            associado.setTipoAssociado((ETipoAssociado) cmbTipoAssociado.getSelectedItem());

            new NAssociado().salvar(associado);

            JOptionPane.showMessageDialog(null,
                    "Operação efetuado com sucesso!");
            limparTela();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

